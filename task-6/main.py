print_question(
    """
問5.
引数nに対して、

2^(n - 1) * (2 ^ n - 1)

の値を戻り値として返す関数を実装して、nが13だった際の結果を出力せよ。
なお"a^b"は「aのb乗(b th power of a)」という定義とする
"""
)


def calc(n: int) -> int:
    return 2 ** (n - 1) * (2 ** n - 1)


