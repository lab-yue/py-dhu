#!/usr/bin/env python3
from typing import List

CYAN = "\033[96m"
UNDERLINE = "\033[4m"
END = "\033[0m"


def print_question(question):
    return print(f"{UNDERLINE}{CYAN}{question}{END}{END}")


def assert_print(a, b):
    print(f"{a} == {b}")
    assert a == b


print_question(
    """
問1.
inputからの入力を受けて入力した10進数の数値を2進数、8進数、16進数に
それぞれ変換して出力するプログラムを実装し、
動作検証として数値101に対してそれぞれ変換して出力せよ
"""
)


def convert(num: int) -> List[str]:
    return [s.format(num) for s in ["{0:b}", "{0:o}", "{0:x}"]]


[_bin, _oct, _hex] = convert(101)

assert_print(_bin, "1100101")
assert_print(_oct, "145")
assert_print(_hex, "65")


